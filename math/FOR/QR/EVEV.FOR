      SUBROUTINE EVEV(NDIM,N,A,VR,VI,AW,R,IV1,IER)
C***** POPAMMA BCEH COCTBEHHX HAEH  COCTBEHHX BEKTOPOB
C       POBOHO MATP;
C      NDIM - O'BEHHA PAMEPHOCT MATP  A  B OEPATOPE DIMENSION
C      N    - CTHHA PAMEPHOCT MATP;
C      A    - CXOHA MATPA;
C      VR   - MACCB BEECTBEHHX ACTE COCTBEHHX HAEH;
C      VI   - MACCB MHMX ACTE COCTBEHHX HAEH;
C      AW   - MATPA COCTBEHHX BEKTOPOB, KOTOPE PACOOEH O
C             CTOAM. KOMEKCHO-COPEHH AP BEKTOPOB PECTAB-
C             ET TOKO OH BEKTOP, COOTBETCTB COCTBEHHOM HA-
C             EH C OOTEHO MHMO ACT ( I- CTOE - BE-
C             ECTBEHHA ACT, I+1 -   -  MHMA ACT );
C      R,IV1- PAOE MACCB;
C      IER  - BXOHO APAMETP ( IER = 0 OTBEAET HOPMAHOM BXO )
C      BHTPEHHE OPOPAMM: BALAN,ELMHE,ELTRA,HQR2,BALBA .
C*****
      REAL A(NDIM,NDIM),AW(NDIM,NDIM),VR(N),VI(N),R(N)
      INTEGER IV1(N)
      CALL BALAN(NDIM,N,A,LOW,IGH,R)
      CALL ELMHE(NDIM,N,LOW,IGH,A,IV1)
      CALL ELTRA(NDIM,N,LOW,IGH,A,IV1,AW)
      CALL HQR2(NDIM,N,LOW,IGH,A,VR,VI,AW,IER)
      CALL BALBA(NDIM,N,LOW,IGH,R,N,AW)
      RETURN
      END
