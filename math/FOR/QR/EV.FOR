      SUBROUTINE EV(NDIM,NA,A,VR,VI,R,IV1,IER)
C***** POPAMMA BCEH COCTBEHHX HAEH POBOHO MATP
C      NDIM - O'BEHHA PAMEPHOCT MATP  A  B OEPATOPE DIMENSION
C      NA   - CTHHA PAMEPHOCT MATP;
C      A    - CXOHA MATPA. HAEH EMEHTOB HE COXPAHTC;
C      VR   - MACCB BEECTBEHHX ACTE COCTBEHHX HAEH;
C      VI   - MACCB MHMX ACTE COCTBEHHX HAEH;
C      R,IV1- PAOE MACCB;
C      IER  - BXOHO APAMETP ( IER=0 OTBEAET HOPMAHOM BXO );
C      BHTPEHHE OPOPAMM: BALAN,ELMHE,HQR .
C*****
      DIMENSION A(NDIM,NDIM),VR(NA),VI(NA),R(NDIM),IV1(NDIM)
      CALL BALAN(NDIM,NA,A,LOW,IGH,R)
      CALL ELMHE(NDIM,NA,LOW,IGH,A,IV1)
      CALL HQR(NDIM,NA,LOW,IGH,A,VR,VI,IER)
      RETURN
      END
