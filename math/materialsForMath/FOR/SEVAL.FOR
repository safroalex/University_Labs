      REAL FUNCTION SEVAL(N,U,X,Y,B,C,D)
      INTEGER N
      REAL U,X(N),Y(N),B(N),C(N),D(N)
C
C  TA OPOPAMMA BCET HAEHE KECKOO
C  CAHA
C
C  SEVAL=Y(I)+B(I)*(U-X(I))+C(I)*(U-X(I))**2+
C             D(I)*(U-X(I))**3
C
C  E X(I).LT.U.LT.X(I+1). COETC CXEMA
C  OPHEPA
C
C  EC U.LT.X(1), TO EPETC HAEHE I=1.
C  EC U.GE.X(N), TO EPETC HAEHE I=N.
C
C  BXOHA HOPMA.
C
C     N     -CO AAHHX TOEK
C     U     -ACCCA,  KOTOPO BCETC HAEHE
C            CAHA
C     X,Y   -MACCB AAHHX ACCC  OPHAT
C     B,C,D -MACCB KOEHTOB CAHA, BCEHHE
C            OPOPAMMO SPLINE
C
C  EC O CPABHEH C PEM BOBOM U HE
C  HAXOTC B TOM E HTEPBAE, TO  PACKAH
C  HHOO HTEPBAA PMEHETC BOH OCK.
C
      INTEGER I,J,K
      REAL DX
      DATA I/1/
      IF(I.GE.N) I=1
      IF(U.LT.X(I)) GO TO 10
      IF(U.LE.X(I+1)) GO TO 30
C
C  BOH OCK
C
 10   I=1
      J=N+1
 20   K=(I+J)/2
      IF(U.LT.X(K))J=K
      IF(U.GE.X(K))I=K
      IF(J.GT.I+1)GO TO 20
C
C  BCT CAH
C
 30   DX=U-X(I)
      SEVAL=Y(I)+DX*(B(I)+DX*(C(I)+DX*D(I)))
      RETURN
      END
