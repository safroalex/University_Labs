      REAL FUNCTION FMIN(AX,BX,F,TOL)
      REAL AX,BX,F,TOL
C
C     BCET PEHE X K TOKE, E F OCTAET
C     MHMMA HA HTEPBAE (AX,BX)
C
C     BXOHA HOPMA.
C
C     AX   EB KOHE CXOHOO HTEPBAA
C     BX   PAB KOHE CXOHOO HTEPBAA
C     F    OPOPAMMA-HK, KOTOPA BCET F(X)
C           OO X B HTEPBAE (AX,BX)
C    TOL   EAEMA HA HTEPBAA HEOPEEEHHOCT
C          KOHEHOO PETATA (.GE.0.0)
C
C    BXOHA HOPMA.
C
C    FMIN  ACCCA, APOKCMPA TOK,
C          E F OCTAET MHMMA
C
C       METO COET KOMHA OCKA OOTOO CEEH
C     OCEOBATEHO APAOECKO HTEPO. CXO-
C    MOCT HKOA HE BAET HATEHO XE, EM 
C    OCKA OHA. EC F MEET HEPEPBH BTOP
C    POBOH, OOTEH B TOKE MHMMA (HE
C    COBAAE H C AX,H C BX), TO CXOMOCT CBEPX-
C    HEHA  OHO MEET OPOK PMEPHO 1.324...
C       HK F HKOA HE BCETC B BX TOKAX,
C    OTCTOX P OT PA MEHEE EM HA EPS*ABS(X)+(TOL/3),
C    E EPS PTEHO PABHO KBAPATHOM KOPH 
C    OTHOCTEHO MAHHO TOHOCT. EC F-HMOAHA
C    HK  BCEHHE HAEH F COXPAHT HMOA-
C    HOCT P COEH KAAHHOO COB PAEEHHOCT,
C    TO FMIN APOKCMPET ACCC OAHOO MHMMA F
C    HA HTEPBAE (AX,BX) C OKO, MEHE 3*EPS*ABS(X)+TOL.
C    EC F HE BETC HMOAHO, TO FMIN MOET C TO E
C    TOHOCT APOKCMPOBAT OKAH MHMM, BOMOHO,
C    HE COBAA C OAHM.
C       TA OPOPAMMA-HK BETC CEKA MOPO-
C    BAHHO BEPCE AO 60-POEP LOCALMIN, PBEEHHO
C    B KHE RICARD BRENT, ALGORITHMS FOR MINIMIZATION
C    WITHOUT DERIVATIVES, PRENTICE-HALL, INC.(1973).
C
      REAL A,B,C,D,E,EPS,XM,P,Q,R,TOL1,TOL2,U,V,W
      REAL FU,FV,FW,FX,X,ABS,SIGN
C
C     C ECT BOBEEHHA B KBAPAT BEHA,
C     OPATHA K OOTOM CEEH
C
      C=0.5*(3.0-SQRT(5.0))
C
C     EPS PTEHO PABHO KBAPATHOM KOPH 
C     OTHOCTEHO MAHHO TOHOCT
C
      EPS=1.0
   10 EPS=EPS/2.0
      TOL1=1.0+EPS
      IF(TOL1.GT.1.0) GO TO 10
      EPS=SQRT(EPS)
C
C     PCBOEHE HAAHX HAEH
C
      A=AX
      B=BX
      V=A+C*(B-A)
      W=V
      X=V
      E=0.0
      FX=F(X)
      FV=FX
      FW=FX
C
C     EC HAHAETC OCHOBHO K
C
   20 XM=0.5*(A+B)
      TOL1=EPS*ABS(X)+TOL/3.0
      TOL2=2.0*TOL1
C
C     POBEPT KPTEP OKOHAH
C
      IF(ABS(X-XM).LE.(TOL2-0.5*(B-A))) GO TO 90
C
C     HEOXOMO  OOTOE CEEHE
C
      IF(ABS(E).LE.TOL1) GO TO 40
C
C     OCTPOT APAO
C
      R=(X-W)*(FX-FV)
      Q=(X-V)*(FX-FW)
      P=(X-V)*Q-(X-W)*R
      Q=2.0*(Q-R)
      IF(Q.GT.0.0) P=-P
      Q=ABS(Q)
      R=E
      E=D
C
C     PEMEMA  APAOA
C
   30 IF(ABS(P).GE.ABS(0.5*Q*R)) GO TO 40
      IF(P.LE.Q*(A-X)) GO TO 40
      IF(P.GE.Q*(B-X)) GO TO 40
C
C     A APAOECKO HTEPO
C
      D=P/Q
      U=X+D
C
C     F HE CEET BCT CKOM KO K AX  BX
C
      IF((U-A).LT.TOL2) D=SIGN(TOL1,XM-X)
      IF((B-U).LT.TOL2) D=SIGN(TOL1,XM-X)
      GO TO 50
C
C     A OOTOO CEEH
C
   40 IF(X.GE.XM) E=A-X
      IF(X.LT.XM)E=B-X
      D=C*E
C
C     F HE CEET BCT CKOM KO K X
C
   50 IF(ABS(D).GE.TOL1) U=X+D
      IF(ABS(D).LT.TOL1) U=X+SIGN(TOL1,D)
      FU=F(U)
C
C     PCBOT HOBE HAEH APAMETPAM A,B,V,W  X
C
      IF(FU.GT.FX) GO TO 60
      IF(U.GE.X) A=X
      IF(U.LT.X) B=X
      V=W
      FV=FW
      W=X
      FW=FX
      X=U
      FX=FU
      GO TO 20
C
   60 IF(U.LT.X) A=U
      IF(U.GE.X) B=U
      IF(FU.LE.FW) GO TO 70
      IF(W.EQ.X) GO TO 70
      IF(FU.LE.FV) GO TO 80
      IF(V.EQ.X) GO TO 80
      IF(V.EQ.W) GO TO 80
      GO TO 20
C
   70 V=W
      FV=FW
      W=U
      FW=FU
      GO TO 20
C
   80 V=U
      FV=FU
      GO TO 20
C
C     KOHE OCHOBHOO KA
C
   90 FMIN=X
      RETURN
      END
