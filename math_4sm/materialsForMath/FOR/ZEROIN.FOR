     REAL FUNCTION ZEROIN(AX,BX,F,TOL)
      REAL AX,BX,F,TOL
C
C     H HK F(X) BCETC B HTEPBAE AX,BX
C
C     BXOHA HOPMA..
C
C     AX     EB KOHE CXOHOO HTEPBAA
C     BX     PAB KOHE CXOHOO HTEPBAA
C     F      OPOPAMMA-HK, KOTOPA BCET F(X)
C             OO X B HTEPBAE AX BX
C     TOL    EAEMA HA HTEPBAA HEOPEEEHHOCT
C            KOHEHOO PETATA
C
C     BXOHA HOPMA...
C
C     ZEROIN ACCCA, APOKCMPA H HK F B
C            HTEPBAE AX, BX
C
C        E POBEPK PEOAAETC, TO F(AX)  F(BX) MET
C     POTBOOOHE HAK.
C        ZEROIN BCET H X B AAHHOM HTEPBAE AX, BX
C     B PEEAX OCKA HA OK  4*MACHEPS*ABS(X) + TOL,
C     E MACHEPS-OTHOCTEHA MAHHA TOHOCT.
C        TA OPOPAMMA-HK PECTABET COO CEKA
C     MOPOBAHH TPAHC AO 60-POEP ZERO,
C     PBEEHHO B KHE RICHARD BRENT, ALGORITHMS FOR
C     MINIMIZATION WITHOUT DERIVATIVES,PRENTICE HALL,INC.(1973).
C
      REAL A,B,C,D,E,EPS,FA,FB,FC,TOL1,XM,P,Q,R,S
C
C     BCT EPS,OTHOCTEH MAHH TOHOCT
C
      EPS=1.0
   10 EPS=EPS/2.0
      TOL1=1.0+EPS
      IF(TOL1.GT.1.0) GO TO 10
C
C     PCBOEHE HAAHX HAEH
C
      A=AX
      B=BX
      FA=F(A)
      FB=F(B)
C
C     HAAT A
C
   20 C=A
      FC=FA
      D=B-A
      E=D
   30 IF(ABS(FC).GE.ABS(FB)) GO TO 40
      A=B
      B=C
      C=A
      FA=FB
      FB=FC
      FC=FA
C
C     POBEPKA CXOMOCT
C
   40 TOL1=2.0*EPS*ABS(B)+0.5*TOL
      XM=0.5*(C-B)
      IF(ABS(XM).LE.TOL1) GO TO 90
C
C     HEOXOMA  CEK
C
      IF(FB.EQ.0.0) GO TO 90
      IF(ABS(E).LT.TOL1) GO TO 70
      IF(ABS(FA).LE.ABS(FB)) GO TO 70
C
C     BOMOHA  KBAPATHA HTEPO
C
      IF(A.NE.C)GO TO 50
C
C     HEHA HTEPO
C
      S=FB/FA
      P=2.0*XM*S
      Q=1.0-S
      GO TO 60
C
C     OPATHA KBAPATHA HTEPO
C
   50 Q=FA/FC
      R=FB/FC
      S=FB/FA
       P=S*(2.0*XM*Q*(Q-R)-(B-A)*(R-1.0))
      Q=(Q-1.0)*(R-1.0)*(S-1.0)
C
C     BPAT HAK
C
   60 IF(P.GT.0.0) Q=-Q
      P=ABS(P)
C
C     PEMEMA  HTEPO
C
      IF((2.0*P).GE.(3.0*XM*Q-ABS(TOL1*Q))) GO TO 70
      IF(P.GE.ABS(0.5*E*Q)) GO TO 70
      E=D
      D=P/Q
      GO TO 80
C
C     CEK
C
   70 D=XM
      E=D
C
C     ABEPT A
C
   80 A=B
      FA=FB
      IF(ABS(D).GT.TOL1) B=B+D
      IF(ABS(D).LE.TOL1) B=B+SIGN(TOL1,XM)
      FB=F(B)
      IF((FB*(FC/ABS(FC))).GT.0.0) GO TO 20
      GO TO 30
C
C     KOHEHO
C
   90 ZEROIN=B
      RETURN
      END
