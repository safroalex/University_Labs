      SUBROUTINE QUANC8(FUN,A,B,ABSERR,RELERR,RESULT,
     *                  ERREST,NOFUN,FLAG)
C
      REAL FUN,A,B,ABSERR,RELERR,RESULT,ERREST,FLAG
      INTEGER NOFUN
C
C     OEHT HTEPA  FUN(X) OT A O B C AAHHO
C     OOBATEEM TOHOCT
C     ABTOMATECKA AATBHA POPAMMA, OCHOBAHHA HA
C     OPME HTOHA-KOTEA 8-OO OPKA
C
C     BXOHA HOPMA
C
C    FUN    -M BHEHE HK,PEAE
C            BCEHE OHTEPAHO HK
C
C    A      -HH  PEE HTEPPOBAH
C
C    B      -BEPXH PEE HTEPPOBAH (B MOET
C            T MEHE, EM  A)
C
C    RELERR -OTHOCTEHA EAEMA OPEHOCT
C
C    ABSERR -ACOTHA EAEMA  OPEHOCT
C
C    BXOHA HOPMA
C
C    RESULT -PEHHOE HAEHE HTEPAA
C
C    ERREST -OEHKA BEH ECTBTEHO OK
C
C    NOFUN  -CO HAEH HK,COOBAHHX
C            P BCEH HTEPAA.
C
C    FLAG   -HKATOP HAEHOCT. EC FLAG PABEH H,
C            TO RESULT OBETBOPET AAHHO PAHE
C            OPEHOCT. EC FLAG=XXX.YYY, TO  XXX-CO
C            HTEPBAOB  KOTOPX HE O CXOMOCT,
C            A 0.YYY-O OCHOBHOO HTEPBAA, OCTABAC
C             OPAOTK B TOT MOMEHT,KOA OPOPAMMA
C            PAC K PEEHOM C  NOFUN.
C
      REAL W0,W1,W2,W3,W4,AREA,X0,F0,STONE,STEP,COR11,TEMP
      REAL QPREV,QNOW,QDIFF,QLEFT,ESTERR,TOLERR
      REAL QRIGHT(31),F(16),X(16),FSAVE(8,30),XSAVE(8,30)
C
C     *** TA1 *** PCBOEHE HAAHX HAEH
C     EPEMEHHM, HE ABCM OT HTEPBAA.
C     EHEPPOBAHE KOHCTAHT.
C
      INTEGER LEVMIN,LEVMAX,LEVOUT,NOMAX,NOFIN,LEV,NIM,I,J
      LEVMIN=1
      LEVMAX=30
      LEVOUT=6
      NOMAX=5000
      NOFIN=NOMAX-8*(LEVMAX-LEVOUT+2**(LEVOUT+1))
C
C     EC NOFUN OCTAET HAEH NOFIN, TO TPEBOA
C
      W0=3956.0/14175.0
      W1=23552.0/14175.0
      W2=-3712.0/14175.0
      W3=41984.0/14175.0
      W4=-18160.0/14175.0
C
C     PCBOT HEBE HAEH EPEMEHHM CMMAM
C
      FLAG=0.0
      RESULT=0.0
      COR11=0.0
      ERREST=0.0
      AREA=0.0
      NOFUN=0
      IF(A.EQ.B)RETURN
C
C     *** TA 2 *** PCBOEHE HAAHX HAEH
C     EPEMEHHM, ABCM OT HTEPBAA, B
C     COOTBETCTB C EPBM HTEPBAOM
C
      LEV=0
      NIM=1
      X0=A
      X(16)=B
      QPREV=0.0
      F0=FUN(X0)
      STONE=(B-A)/16.0
      X(8)=(X0+X(16))/2.0
      X(4)=(X0+X(8))/2.0
      X(12)=(X(8)+X(16))/2.0
      X(2)=(X0+X(4))/2.0
      X(6)=(X(4)+X(8))/2.0
      X(10)=(X(8)+X(12))/2.0
      X(14)=(X(12)+X(16))/2.0
      DO 25 J=2,16,2
      F(J)=FUN(X(J))
   25 CONTINUE
      NOFUN=9
C
C     *** TA 3 *** OCHOBHE BCEH
C     TPETC QPREV, X0, X2, X4,...,X16, F0, F2, F4,...
C     ,F16. BCTC X1, X3,...,X15, F1, F3,...,F15,
C     QLEFT,QRIGHT,QNOW,QDIF,AREA
C
   30 X(1)=(X0+X(2))/2.0
      F(1)=FUN(X(1))
      DO 35 J=3,15,2
      X(J)=(X(J-1)+X(J+1))/2.0
      F(J)=FUN(X(J))
   35 CONTINUE
      NOFUN=NOFUN+8
      STEP=(X(16)-X0)/16.0
      QLEFT=(W0*(F0+F(8))+W1*(F(1)+F(7))+W2*
     *      (F(2)+F(6))+W3*(F(3)+F(5))+W4*F(4))*STEP
      QRIGHT(LEV+1)=(W0*(F(8)+F(16))+W1*(F(9)+F(15))+
     *       W2*(F(10)+F(14))+W3*(F(11)+F(13))+W4*F(12))*STEP
      QNOW=QLEFT+QRIGHT(LEV+1)
      QDIFF=QNOW-QPREV
      AREA=AREA+QDIFF
C
C     *** TA 4 *** POBEPKA CXOMOCT  HTEPBAA
C
      ESTERR=ABS(QDIFF)/1023.0
      TOLERR=AMAX1(ABSERR,RELERR*ABS(AREA))*(STEP/STONE)
      IF(LEV.LT.LEVMIN)GO TO 50
      IF(LEV.GE.LEVMAX)GO TO 62
      IF(NOFUN.GT.NOFIN)GO TO 60
      IF(ESTERR.LE.TOLERR)GO TO 70
C
C     *** TA 5 *** CXOMOCT HET.
C     CTAHOBT CE HTEPBA
C
   50 NIM=2*NIM
      LEV=LEV+1
C
C     AOMHT EMEHT, OTHOCEC K PABO OOBHE
C     HTEPBAA,  EO COOBAH.
C
      DO 52 I=1,8
      FSAVE(I,LEV)=F(I+8)
      XSAVE(I,LEV)=X(I+8)
   52 CONTINUE
C
C     COPAT EMEHT, OTHOCEC K EBO OOBHE
C     HTEPBAA  HEMEEHHOO COOBAH
C
      QPREV=QLEFT
      DO 55 I=1,8
      J=-I
      F(2*J+18)=F(J+9)
      X(2*J+18)=X(J+9)
   55 CONTINUE
      GO TO 30
C
C     *** TA 6 *** "OAPH" PAE
C     CO HAEH HK KO K TOM, TO
C     PEBCT CTAHOBEHH PEE.
C
   60 NOFIN=2*NOFIN
      LEVMAX=LEVOUT
      FLAG=FLAG+(B-X0)/(B-A)
      GO TO 70
C
C     TEKEE PEEHOE HAEHE H EEH
C     OOAM PABHO LEVMAX
C
   62 FLAG=FLAG+1.0
C
C     *** TA 7 *** CXOMOCT  HTEPBAA MEET MECTO
C     PABT OEPEHE CAAEME K EPEMEHHM CMMAM.
C
   70 RESULT=RESULT+QNOW
      ERREST=ERREST+ESTERR
      COR11=COR11+QDIFF/1023.0
C
C     CTAHOBT CE HTEPBA.
C
   72 IF(NIM.EQ.2*(NIM/2))GO TO 75
      NIM=NIM/2
      LEV=LEV-1
      GO TO 72
   75 NIM=NIM+1
      IF(LEV.LE.0)GO TO 80
C
C     COPAT EMEHT, HEOXOME  CEEO HTEBAA
C
      QPREV=QRIGHT(LEV)
      X0=X(16)
      F0=F(16)
      DO 78 I=1,8
      F(2*I)=FSAVE(I,LEV)
      X(2*I)=XSAVE(I,LEV)
   78 CONTINUE
      GO TO 30
C
C     *** TA 8 *** AKTEHE OEPA  BXO
C
   80 RESULT=RESULT+COR11
C
C     OECET, TO HAEHE EPEMEHHO ERREST
C     O HE MEHE POBH OKPEH
C
      IF(ERREST.EQ.0.0)RETURN
   82 TEMP=ABS(RESULT)+ERREST
      IF(TEMP.NE.ABS(RESULT))RETURN
      ERREST=2.0*ERREST
      GO TO 82
      END
